# --- 3. معالجة النصوص (الذكاء اللغوي) ---

def normalize_text(text):
    """تنظيف ذكي للنصوص مع حماية أسماء الأحياء"""
    if not text: return ""

    # 1. تنظيف أساسي
    text = text.strip().lower()
    
    # 2. إزالة التشكيل
    text = re.sub(r"[\u064B-\u0652]", "", text)

    # 3. توحيد الحروف (تمت إضافة الألف المقصورة ى -> ي)
    replacements = {
        "أ": "ا", "إ": "ا", "آ": "ا",
        "ة": "ه", "ى": "ي", "ئ": "ي", "ؤ": "و",
        "ڤ": "ف", "گ": "ك" # إضافات احتياطية
    }
    for old, new in replacements.items():
        text = text.replace(old, new)

    # 4. إزالة تكرار الحروف (مشوااااار -> مشوار)
    text = re.sub(r'(.)\1+', r'\1', text)

    words = text.split()
    clean_words = []

    for w in words:
        # --- التعديل الجوهري (الحماية) ---
        
        # إزالة "الواو" و"الباء" فقط إذا ارتبطت بـ "ال" التعريف (مثل: والزهراء -> زهراء)
        # أو إذا كانت الكلمة طويلة جداً (أكثر من 5 حروف) لتقليل خطر تخريب كلمات مثل "وادي"
        if (w.startswith("وال") or w.startswith("بال") or w.startswith("كال")) and len(w) > 4:
            w = w[1:] # نحذف الحرف الأول فقط (و، ب، ك) ونترك "ال" ليعالجها الكود بالأسفل

        # معالجة "ال" التعريف
        # الشرط: أن تكون الكلمة بعد الحذف أطول من حرفين (للحفاظ على كلمات مثل "الر")
        # وادي (4 حروف) -> لن تتأثر
        if w.startswith("ال") and len(w) > 4: 
            clean_words.append(w[2:])
        else:
            clean_words.append(w)

    return " ".join(clean_words)
